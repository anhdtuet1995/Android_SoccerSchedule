public class Team {

    private String name;

    public String getName() { return this.name; }

    public void setName(String name) { this.name = name; }

    private String code;

    public String getCode() { return this.code; }

    public void setCode(String code) { this.code = code; }

    private String shortName;

    public String getShortName() { return this.shortName; }

    public void setShortName(String shortName) { this.shortName = shortName; }

    private String squadMarketValue;

    public String getSquadMarketValue() { return this.squadMarketValue; }

    public void setSquadMarketValue(String squadMarketValue) { this.squadMarketValue = squadMarketValue; }

    private String crestUrl;

    public String getCrestUrl() { return this.crestUrl; }

    public void setCrestUrl(String crestUrl) { this.crestUrl = crestUrl; }

}
